namespace Delegates.classe;

public class delegates
{
    public void Somar(int n1, int n2)
    {
        Console.WriteLine(n1 + n2);
    }
    public void Subtrair(int n1, int n2)
    {
        Console.WriteLine(n1 - n2);
    }
    public void Multiplica(int n1, int n2)
    {
        Console.WriteLine(n1 * n2);
    }
}